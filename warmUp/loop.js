
// loop 1
for( let i = 0 ; i < 5 ; i ++ ) {
    for( j = 0 ; j < 5 ; j ++) {
        console.log('I : '+i+" J : "+j);
    }
}


// loop 2
for( let i = 0 ; i < 3 ; i ++ ) {
    for( j = 0 ; j < i ; j ++) {
        console.log('I : '+i+" J : "+j);
    }
}


// loop 3
for( let i = 0 ; i < 3 ; i ++ ) {
    for( j = i ; j > 0 ; j -- ) {
        console.log('I : '+i+" J : "+j);
    }
}